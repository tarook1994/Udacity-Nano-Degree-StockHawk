    private void setData(ArrayList list) {

        ArrayList<Entry> values = new ArrayList<Entry>();
        Log.d("size data",list.size()+"");

        for (int i = 0; i < list.size(); i++) {
            if(list.size()!=0){
                float val = (float) list.get(i);
                values.add(new Entry(i, val, getResources().getDrawable(R.drawable.fab_plus)));
            } else {
                values.add(new Entry(i, 0, getResources().getDrawable(R.drawable.fab_plus)));
            }

        }

        LineDataSet set1;

        if (mChart.getData() != null &&
                mChart.getData().getDataSetCount() > 0) {
            set1 = (LineDataSet)mChart.getData().getDataSetByIndex(0);
            set1.setValues(values);
            mChart.getData().notifyDataChanged();
            mChart.notifyDataSetChanged();
            Log.d("Where","if");
        } else {
            Log.d("Where","else");
            // create a dataset and give it a type
            set1 = new LineDataSet(values, getIntent().getStringExtra("stock")+" Monthly Curve");

            //set1.setDrawIcons(false);

            // set the line to be drawn like this "- - - - - -"
            set1.enableDashedLine(10f, 5f, 0f);
            set1.enableDashedHighlightLine(10f, 5f, 0f);
            set1.setColor(Color.BLACK);
            set1.setCircleColor(Color.BLACK);
            set1.setLineWidth(1f);
            set1.setCircleRadius(3f);
            set1.setDrawCircleHole(false);
            set1.setValueTextSize(9f);
            set1.setDrawFilled(true);
            set1.setFormLineWidth(1f);
            set1.setFormLineDashEffect(new DashPathEffect(new float[]{10f, 5f}, 0f));
            set1.setFormSize(15.f);

            if (Utils.getSDKInt() >= 18) {
                // fill drawable only supported on api level 18 and above
                Drawable drawable = ContextCompat.getDrawable(this, R.drawable.fab_plus);
                set1.setFillDrawable(drawable);
            }
            else {
                set1.setFillColor(Color.BLACK);
            }

            ArrayList<ILineDataSet> dataSets = new ArrayList<ILineDataSet>();
            dataSets.add(set1); // add the datasets

            // create a data object with the datasets
            LineData data = new LineData(dataSets);

            // set data
            mChart.setData(data);
            if(list.size()!=0){
                mChart.invalidate();
            }

        }
    }

    class getHistory extends AsyncTask<String,Void,Void>{
        ArrayList<Float> history = new ArrayList<>();
        ArrayList<String> historyDates = new ArrayList<>();
        final ProgressDialog progressDialog = new ProgressDialog(LineChartActivity.this);

        @Override
        protected void onPostExecute(Void aVoid) {
            super.onPostExecute(aVoid);
            progressDialog.dismiss();
            setData(history);
            String[] values = new String[historyDates.size()];
            if(values.length != 0){
                for(int i=0;i<historyDates.size();i++){
                    Log.d("Element",historyDates.get(i));

                    String year ="20"+historyDates.get(i).split(",")[0].substring(1);
                    String month =historyDates.get(i).split(",")[1];
                    if(month.equals("0")){
                        month ="12";
                    }
                    String date = month+"/"+year;
                    values[i] = date;
                }
                XAxis xAxis = mChart.getXAxis();
                xAxis.enableGridDashedLine(10f, 10f, 0f);
                xAxis.setValueFormatter(new MyXAxisValueFormatter(values));

            } else {
                Log.d("ana","else");
            }




        }

        @Override
        protected Void doInBackground(String... params) {
            progressDialog.setIndeterminate(true);
            progressDialog.setMessage("Please Wait While Loading Data...");
            progressDialog.setCancelable(false);
            runOnUiThread(new Runnable() {
                @Override
                public void run() {
                    progressDialog.show();
                }
            });

            history = QuoteSyncJob.getQuotesHistory(getApplicationContext(),params[0]);
            historyDates = QuoteSyncJob.getQuotesHistoryDates(getApplicationContext(),params[0]);
            return null;
        }
    }
    public class MyXAxisValueFormatter implements IAxisValueFormatter {

        private String[] mValues;

        public MyXAxisValueFormatter(String[] values) {
            this.mValues = values;
        }

        @Override
        public String getFormattedValue(float value, AxisBase axis) {
            // "value" represents the position of the label on the axis (x or y)
            return mValues[(int) value];
        }

    }
